  // ============================================================ compiler (graph -> GLSL fragment body)
  function compileBody(graph) {
    const nodes = graph.nodes, outId = graph.out;
    const order = [], seen = {};
    (function visit(id, stack) {
      if (!id || !nodes[id] || seen[id] || stack[id]) return;
      stack[id] = 1;
      const spec = NODES[nodes[id].type];
      if (spec) for (const port of spec.ins) visit(nodes[id].ins && nodes[id].ins[port], stack);
      stack[id] = 0; seen[id] = 1; order.push(id);
    })(outId, {});
    let body = '';
    const varOf = {};
    for (const id of order) {
      const n = nodes[id], spec = NODES[n.type]; if (!spec) continue;
      const IN = {};
      for (const port of spec.ins) { const src = n.ins && n.ins[port]; IN[port] = (src && varOf[src]) ? { v: varOf[src], ret: NODES[nodes[src].type].ret } : null; }
      if (spec.ret === 'out') { body += '  ' + spec.glsl(n.props, IN) + '\n'; continue; }
      const v = 'n_' + id.replace(/[^a-zA-Z0-9_]/g, '_');
      varOf[id] = v;
      body += '  ' + (spec.ret === 'vec3' ? 'vec3 ' : 'float ') + v + ' = ' + spec.glsl(n.props, IN) + ';\n';
    }
    // a graph with no reachable output draws nothing (fully transparent)
    if (!nodes[outId] || NODES[nodes[outId].type].ret !== 'out') body += '  gl_FragColor = vec4(0.0);\n';
    return body;
  }
