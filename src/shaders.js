// MOSSVEIL — shaders.js : node-based shader graph -> fullscreen screen-effect overlay (G.Shaders).
// Roadmap #78. NET-NEW render feature & the FIRST authorable GLSL surface in the project.
//
// A "graph" is a little DAG of typed nodes (sources · generators · math · colour · output). Each node
// type knows how to emit BOTH a GLSL expression AND an equivalent CPU (JS) value, so:
//   • the editor previews the graph on the CPU (no second WebGL context — exactly like the parallax /
//     decals tools), and
//   • the game compiles the SAME graph to a fragment shader and draws it as a transparent FULLSCREEN
//     overlay composited on top of the final frame (animated fog, scanlines, pulsing vignettes, colour
//     washes…). Blend (normal/additive) and opacity are per-graph.
//
// Graphs are authored in data/shaders.js (G.SHADERS_DATA). `active` names the one graph applied in
// game ('' = none → the overlay does NOTHING and the frame is byte-identical to before this feature).
// The single game seam is one guarded `G.Shaders.renderOverlay(dt)` after the post pass. No THREE /
// document at LOAD time (gen-data node-evals this for the default overlay); every node's GLSL is
// generated by a small compiler so it ALWAYS type-checks (floats and vec3s auto-convert), and material
// creation is try/caught so a bad graph can never crash the renderer.
