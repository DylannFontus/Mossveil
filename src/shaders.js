  const TAU = Math.PI * 2;

  // ============================================================ GLSL formatting helpers
  function glf(n) { n = +n; if (!isFinite(n)) n = 0; let s = '' + n; if (!/[.eE]/.test(s)) s += '.0'; return s; }
  function gli(n) { return '' + (n | 0); }

  // ============================================================ node registry
  // Each node: { label, cat, ret:'float'|'vec3'|'out', ins:[portNames], props:{key:[def,min,max,int?]},
  //              glsl(p, IN), cpu(p, IN, ctx) }  where IN[port] is { v, ret } (glsl) or a value (cpu).
  // asF / asV auto-convert between float and vec3 so any wire is legal; a missing input uses `dflt`.
  function asF(e, d) { if (!e) return glf(d); return e.ret === 'vec3' ? '_lum(' + e.v + ')' : e.v; }
  function asV(e, d) { if (!e) return 'vec3(' + glf(d) + ')'; return e.ret === 'float' ? 'vec3(' + e.v + ')' : e.v; }
  function cF(v, d) { if (v == null) return d; return Array.isArray(v) ? (0.299 * v[0] + 0.587 * v[1] + 0.114 * v[2]) : v; }
  function cV(v, d) { if (v == null) return [d, d, d]; return Array.isArray(v) ? v : [v, v, v]; }
  const cl01 = x => Math.max(0, Math.min(1, x));
  const sstep = (a, b, x) => { if (a === b) return x < a ? 0 : 1; const t = cl01((x - a) / (b - a)); return t * t * (3 - 2 * t); };

  // CPU procedural noise (visually mirrors the GLSL prelude; need not be bit-identical)
  function jhash(x, y) { const s = Math.sin(x * 127.1 + y * 311.7) * 43758.5453123; return s - Math.floor(s); }
  function jnoise(x, y) {
    const ix = Math.floor(x), iy = Math.floor(y), fx = x - ix, fy = y - iy;
    const a = jhash(ix, iy), b = jhash(ix + 1, iy), c = jhash(ix, iy + 1), d = jhash(ix + 1, iy + 1);
    const ux = fx * fx * (3 - 2 * fx), uy = fy * fy * (3 - 2 * fy);
    return a * (1 - ux) * (1 - uy) + b * ux * (1 - uy) + c * (1 - ux) * uy + d * ux * uy;
  }
  function jfbm(x, y, oct) { let s = 0, a = 0.5; for (let i = 0; i < oct; i++) { s += a * jnoise(x, y); x *= 2; y *= 2; a *= 0.5; } return s; }
  function jvoro(x, y) {
    const gx = Math.floor(x), gy = Math.floor(y), fx = x - gx, fy = y - gy; let md = 1.5;
    for (let oy = -1; oy <= 1; oy++) for (let ox = -1; ox <= 1; ox++) {
      const rx = ox + jhash(gx + ox, gy + oy) - fx, ry = oy + jhash(gx + ox + 7, gy + oy + 3) - fy;
      md = Math.min(md, rx * rx + ry * ry);
    }
    return cl01(Math.sqrt(md));
  }

